#Operador de incremento:
a = 5
a += 1
print(a)  # 6
# En este ejemplo, utilizamos el operador de asignación con suma (+=) para incrementar el valor de 'a' en 1.



# Operador de decremento:
a = 5
a -= 1
print(a)  # 4
# Aquí utilizamos el operador de asignación con resta (-=) para decrementar el valor de 'a' en 1.

# En resumen, en Python no hay operadores de incremento y decremento específicos (++ y --),
# pero puedes lograr el mismo resultado utilizando los operadores de asignación adecuados (+= y -=) junto con el valor deseado.