# len(): Devuelve la longitud (número de elementos) de un objeto.
cadena = "Hola, mundo!"
longitud = len(cadena)
print(longitud)  # Imprime 12


# type(): Devuelve el tipo de un objeto.
numero = 10
tipo = type(numero)
print(tipo)  # Imprime <class 'int'>


# str(): Convierte un objeto en una cadena de caracteres.
numero = 10
cadena = str(numero)
print(cadena)  # Imprime "10"


# int(): Convierte un objeto en un número entero.
cadena = "10"
numero = int(cadena)
print(numero)  # Imprime 10


# float(): Convierte un objeto en un número de punto flotante.
cadena = "3.14"
numero = float(cadena)
print(numero)  # Imprime 3.14


# list(): Convierte un objeto en una lista.
cadena = "Hola"
lista = list(cadena)
print(lista)  # Imprime ['H', 'o', 'l', 'a']


# max(): Devuelve el valor máximo de una secuencia de elementos.
numeros = [5, 8, 2, 10]
maximo = max(numeros)
print(maximo)  # Imprime 10


# min(): Devuelve el valor mínimo de una secuencia de elementos.
numeros = [5, 8, 2, 10]
minimo = min(numeros)
print(minimo)  # Imprime 2