# Operador "and": Retorna True si ambas expresiones son verdaderas, de lo contrario retorna False.
a = 5
b = 7
c = 3
print(a < b and b < c)  # False


# Operador "or": Retorna True si al menos una de las expresiones es verdadera, de lo contrario retorna False.

a = 5
b = 7
c = 3
print(a < b or b < c)  # True


