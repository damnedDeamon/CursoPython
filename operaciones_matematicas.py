# Suma:

a = 5
b = 3
suma = a + b
print(suma)  # Imprime: 8


# Resta:

c = 5
d = 3
resta = c - d
print(resta)  # Imprime: 2


# Multiplicación:


e = 5
f = 3
multiplicacion = e * f
print(multiplicacion)  # Imprime: 15

# División:

q = 5
w = 3
division = q / w
print(division)  # Imprime: 1.6666666666666667


# División entera:

r = 5
t = 3
division_entera = r // t
print(division_entera)  # Imprime: 1


# Módulo (resto de la división):

y = 5
u = 3
modulo = y % u
print(modulo)  # Imprime: 2
