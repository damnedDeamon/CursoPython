# Operador "in":
lista = [1, 2, 3, 4, 5]
print(3 in lista)  # True
# En este ejemplo, se verifica si el valor 3 está presente en la lista.


# Operador "not in":
cadena = "Hola, mundo"
print("hola" not in cadena)  # True
# Aquí se verifica si la subcadena "hola" no está presente en la cadena de texto.


# Operador "in" con conjunto (set):
conjunto = {1, 2, 3, 4, 5}
print(6 in conjunto)  # False
# En este caso, se verifica si el valor 6 está presente en el conjunto.


# Operador "in" con diccionario (dict):
diccionario = {'a': 1, 'b': 2, 'c': 3}
print('a' in diccionario)  # True
# Aquí se verifica si la clave 'a' está presente en el diccionario.