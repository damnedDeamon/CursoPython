# Operador de asignación simple (=): Asigna un valor a una variable.
a = 5
print(a)  # 5


#Operador de asignación con suma (+=): Suma un valor a una variable y asigna el resultado.
a = 5
a += 2
print(a)  # 7


# Operador de asignación con resta (-=): Resta un valor a una variable y asigna el resultado.
a = 5
a -= 2
print(a)  # 3


# Operador de asignación con multiplicación (*=): Multiplica una variable por un valor y asigna el resultado.
a = 5
a *= 2
print(a)  # 10


# Operador de asignación con división (/=): Divide una variable por un valor y asigna el resultado.
a = 10
a /= 2
print(a)  # 5.0


# Operador de asignación con módulo (%=): Calcula el residuo de la división de una variable por un valor y asigna el resultado.
a = 10
a %= 3
print(a)  # 1