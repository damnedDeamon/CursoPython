# Expresión anidada con operadores aritméticos:
a = 5
b = 7
c = 3
resultado = (a + b) * c
print(resultado)  # 36
# En este ejemplo, se suma 'a' y 'b', y luego se multiplica el resultado por 'c'.


# Expresión anidada con operadores relacionales y lógicos:
a = 5
b = 7
c = 3
resultado = (a < b) and (b < c)
print(resultado)  # False
# En este caso, se compara si 'a' es menor que 'b' y si 'b' es menor que 'c', 
# utilizando los operadores relacionales y luego se utiliza el operador lógico "and" para combinar ambas comparaciones.


#Expresión anidada con operadores lógicos y aritméticos:
a = 5
b = 7
c = 3
resultado = ((a + b) > c) or (a == b)
print(resultado)  # True
# En este ejemplo, se suma 'a' y 'b', se compara si el resultado es mayor que 'c' y luego se combina con la comparación entre 'a' y 'b' utilizando el operador lógico "or".