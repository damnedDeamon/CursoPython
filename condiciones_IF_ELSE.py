# Ejemplo básico:
edad = 18

if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")
# En este ejemplo, se verifica si la variable "edad" es mayor o igual a 18. 
# Si se cumple la condición, se imprime "Eres mayor de edad". De lo contrario, se imprime "Eres menor de edad".



# Ejemplo con condición adicional:
numero = 5

if numero > 0:
    print("El número es positivo")
elif numero < 0:
    print("El número es negativo")
else:
    print("El número es cero")
# En este caso, se utiliza una estructura "if-elif-else" para verificar si el número es positivo, negativo o cero.



# Ejemplo con múltiples condiciones:
puntuacion = 85

if puntuacion >= 90:
    print("Excelente trabajo")
elif puntuacion >= 70:
    print("Buena puntuación")
elif puntuacion >= 50:
    print("Puntuación aceptable")
else:
    print("Debes mejorar")
# Aquí se evalúa la puntuación y se imprime un mensaje según el rango de la puntuación obtenida.